interface Props {
  docId: DocId;
  onBack: () => void;
}

export const DocViewer: React.FC<Props> = ({ docId, onBack }) => {
  const doc = DOCS[docId];
  return (
    <div className="docs-overlay">
      <TitleBar />
      <div className="docs-shell">
        <div className="docs-header">
          <button className="docs-back-btn" onClick={onBack}>
            <ArrowLeft size={16} /> Документация
          </button>
          <div className="docs-header-title">
            <h1>{doc.title}</h1>
          </div>
          <span />
        </div>

        <div className="docs-viewer-content">
          {doc.subtitle && <p className="docs-doc-subtitle">{doc.subtitle}</p>}
          {doc.sections.map((s, i) => (
            <div key={i} className={`docs-section docs-section-l${s.level ?? 2}`}>
              {s.heading && (s.level === 3
                ? <h3 className="docs-section-h3">{s.heading}</h3>
                : <h2 className="docs-section-h2">{s.heading}</h2>
              )}
              {s.text && <p className="docs-section-text">{s.text}</p>}
              {s.items && (
                <ul className="docs-section-list">
                  {s.items.map((item, j) => (
                    <li key={j} className="docs-section-item">
                      {item.split('\n').map((line, k) => (
                        <span key={k} className={k > 0 ? 'docs-item-sub' : ''}>{line}</span>
                      ))}
                    </li>
                  ))}
                </ul>
              )}
              {s.table && (
                <div className="docs-table-wrap">
                  <table className="docs-table">
                    {s.table.headers && (
                      <thead>
                        <tr>{s.table.headers.map((h, j) => <th key={j}>{h}</th>)}</tr>
                      </thead>
                    )}
                    <tbody>
                      {s.table.rows.map((row, j) => (
                        <tr key={j}>{row.map((cell, k) => <td key={k}>{cell}</td>)}</tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              )}
              {s.note && <div className="docs-section-note">ℹ️ {s.note}</div>}
            </div>
          ))}
          <button className="docs-back-bottom-btn" onClick={onBack}>
            <ArrowLeft size={14} /> Назад к документации
          </button>
        </div>
      </div>
    </div>
  );
};
